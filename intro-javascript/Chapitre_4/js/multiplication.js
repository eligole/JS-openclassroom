var table = Number(prompt("entre la table de multiplication"));

while ((table <= 1) || (table >= 10)) {

    if (table === 1) {
        console.log("1x0=");
    }

    if (table === 2) {
        console.log("2x0=");
    }

    if (table === 3) {
        console.log("3x0=");
    }

    if (table === 4) {
        console.log("4x0");
    }

}

